    @Override
    public boolean getMoreResults() throws SQLException {
        return getMoreResults(1);
    }

    @Override
    public boolean getMoreResults(int current) throws SQLException {
        if (currentResultSet instanceof TMultiResultSet) {
            boolean more = ((TMultiResultSet) currentResultSet).getMoreResults(current);
            updateCount = ((TMultiResultSet) currentResultSet).getAffectRows();
            return more;
        } else {
            updateCount = -1;
            return false;
        }
    }

    @Override
    public ResultSet getGeneratedKeys() throws SQLException {
        if (this.executionContext == null
            || this.executionContext.getAutoGeneratedKeys() != java.sql.Statement.RETURN_GENERATED_KEYS) {
            throw new SQLException(
                "Generated keys not requested. You need to specify Statement.RETURN_GENERATED_KEYS to Statement.executeUpdate() or Connection.prepareStatement().");
        }

        ArrayResultCursor result = new ArrayResultCursor("Generated_keys");
        result.addColumn("Generated_key", DataTypes.LongType);
        result.initMeta();

        List<Long> generatedKeys = this.conn.getGeneratedKeys();
        Collections.sort(generatedKeys);
        if (generatedKeys == null || generatedKeys.isEmpty()) {
            long last = this.conn.getReturnedLastInsertId();
            Parameters params = this.executionContext.getParams();
            if (params.isBatch()) {
                int size = this.executionContext.getParams().getBatchSize();
                for (int i = 1; i <= size; i++) {
                    result.addRow(new Object[] {last - size + i});
                }
            } else {
                result.addRow(new Object[] {last});
            }
        } else {
            // 如果是直连下推模式，直接获取mysql返回的keys进行返回
            for (Long id : generatedKeys) {
                result.addRow(new Object[] {id});
            }
        }

        generatedKeysResults = new TResultSet(new ResultCursor(result), null);
        return generatedKeysResults;
    }

