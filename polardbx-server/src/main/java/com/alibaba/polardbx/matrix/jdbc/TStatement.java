    protected int resultSetHoldability = -1;

    protected InputStream localInFileInputStream = null;

    private final String appName;

    public TStatement(TDataSource ds, TConnection tConnection, ExecutionContext executionContext) {
        this.ds = ds;
        this.conn = tConnection;
        this.executionContext = executionContext;
        this.appName = executionContext.getAppName();
    }

    public TStatement(TDataSource ds, TConnection tConnection, ByteString sql, ExecutionContext executionContext) {
        this.ds = ds;
        this.conn = tConnection;
        this.sql = sql;
        this.executionContext = executionContext;
        this.appName = executionContext.getAppName();
    }

    protected void checkClosed() throws SQLException {
        if (closed) {
            throw new SQLException("No operations allowed after statement closed.");
        }
    }

    // jdbc规范: 返回true表示executeQuery，false表示executeUpdate
    @Override
    public boolean execute(String sql) throws SQLException {
        return executeInternal(ByteString.from(sql), -1, null, null);
    }

    @Override
    public boolean execute(String sql, int autoGeneratedKeys) throws SQLException {
        return executeInternal(ByteString.from(sql), autoGeneratedKeys, null, null);
    }

    @Override
    public boolean execute(String sql, int[] columnIndexes) throws SQLException {
        return executeInternal(ByteString.from(sql), -1, columnIndexes, null);
    }

    @Override
    public boolean execute(String sql, String[] columnNames) throws SQLException {
        return executeInternal(ByteString.from(sql), -1, null, columnNames);
    }

    // jdbc规范: 返回true表示executeQuery，false表示executeUpdate
    protected boolean executeInternal(ByteString sql, int autoGeneratedKeys, int[] columnIndexes,
                                      String[] columnNames) throws SQLException {
        checkClosed();
        ensureResultSetIsEmpty();

        OptimizerContext.setContext(this.ds.getConfigHolder().getOptimizerContext());
        executionContext.setAutoGeneratedKeys(autoGeneratedKeys);
        executionContext.setColumnIndexes(columnIndexes);
        executionContext.setColumnNames(columnNames);
        executeSQL(sql);
        if (currentResultSet instanceof TMultiResultSet) {
            updateCount = ((TMultiResultSet) currentResultSet).getAffectRows();
        } else if (currentResultSet instanceof TResultSet) {
            updateCount = ((TResultSet) currentResultSet).getAffectRows();
        } else {
            updateCount = -1;
        }

        // 如果是读操作,updateCount=-1,返回true
        return updateCount < 0;
    }

    /*
     * ========================================================================
     * executeQuery 查询逻辑 这里按照mysql
     * connection逻辑，调用connection的executeSQL方法返回resultset Connection 的 execSQL方法
     * ======================================================================
     */
    protected ResultSet executeQuery(ByteString sql) throws SQLException {
        checkClosed();
        ensureResultSetIsEmpty();
        executeSQL(sql);
        return currentResultSet;
    }

    @Override
    public ResultSet executeQuery(String sql) throws SQLException {
        // Keep compatible with internal usage e.g. DDLNewHandler
        return executeQuery(ByteString.from(sql));
    }

    /*
     * ========================================================================
     * executeUpdate逻辑 这里按照mysql
     * connection逻辑，调用connection的executeSQL方法返回resultset
     * ,然后根据resultset获得affertrows Connection 的 execSQL方法
     * ======================================================================
     */
    @Override
    public int executeUpdate(String sql) throws SQLException {
        return executeUpdateInternal(ByteString.from(sql), -1, null, null);
    }

    @Override
    public int executeUpdate(String sql, int autoGeneratedKeys) throws SQLException {
        return executeUpdateInternal(ByteString.from(sql), autoGeneratedKeys, null, null);
    }

    @Override
    public int executeUpdate(String sql, int[] columnIndexes) throws SQLException {
        return executeUpdateInternal(ByteString.from(sql), -1, columnIndexes, null);
    }

    @Override
    public int executeUpdate(String sql, String[] columnNames) throws SQLException {
        return executeUpdateInternal(ByteString.from(sql), -1, null, columnNames);
    }

    protected int executeUpdateInternal(ByteString sql, int autoGeneratedKeys, int[] columnIndexes,
                                        String[] columnNames) throws SQLException {
        checkClosed();
        ensureResultSetIsEmpty();
        executionContext.setAutoGeneratedKeys(autoGeneratedKeys);
        executionContext.setColumnIndexes(columnIndexes);
        executionContext.setColumnNames(columnNames);
        executeSQL(sql);
        if (currentResultSet instanceof TMultiResultSet) {
            return ((TMultiResultSet) currentResultSet).getAffectRows();
        } else {
            return ((TResultSet) currentResultSet).getAffectRows();
        }
    }

    protected void executeSQL(ByteString sql) throws SQLException {
        currentResultSet = conn.executeSQL(sql, null, this, executionContext);
    }

    @Override
    public void addBatch(String sql) throws SQLException {
        throw new UnsupportedOperationException(
            "addBatch(String sql) is not supported, please use addBatch() in PreparedStatement");
    }

    @Override
    public int[] executeBatch() throws SQLException {
        throw new UnsupportedOperationException("executeBatch");
    }

    @Override
    public void clearBatch() throws SQLException {
        checkClosed();
        throw new UnsupportedOperationException(
            "clearBatch() is not supported, please use clearBatch() in PreparedStatement");
    }

    @Override
    public boolean isClosed() throws SQLException {
        return this.closed;
    }

    @Override
    public void close() throws SQLException {
        close(true);
    }

    void close(boolean removeThis) throws SQLException {
        if (closed) {
            return;
        }
        try {
            if (currentResultSet != null) {
                currentResultSet.close();
            }

            if (removeThis) {
                conn.removeStatement(this);
            }

            if (executionContext.isInternalSystemSql()) {
                // For all stmt generated by drds internal system(e.g. sync
                // xxx/async ddl jobs/ and so on),
                // all the memory pool of these sql should be destroy here
                executionContext.clearAllMemoryPool();
            }
        } catch (Exception e) {
            throw GeneralUtil.nestedException(e);
        } finally {
            currentResultSet = null;
        }
        closed = true;
    }

    /**
     * 如果新建了查询，那么上一次查询的结果集应该被显示的关闭掉。这才是符合jdbc规范的
     */
    protected void ensureResultSetIsEmpty() throws SQLException {
        if (currentResultSet != null) {
            // log.debug("result set is not null,close current result set");
            try {
                currentResultSet.close();
            } catch (SQLException e) {
                log.error("exception on close last result set . can do nothing..", e);
            } finally {
                // 最终要显示的关闭它
                currentResultSet = null;
            }
        }

        if (generatedKeysResults != null) {
            try {
                generatedKeysResults.close();
            } catch (SQLException e) {
                log.error("exception on close last result set . can do nothing..", e);
            } finally {
                // 最终要显示的关闭它
                generatedKeysResults = null;
            }
        }
    }

